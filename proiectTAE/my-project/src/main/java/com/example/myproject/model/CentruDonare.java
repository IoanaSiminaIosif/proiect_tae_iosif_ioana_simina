package com.example.myproject.model;

public class CentruDonare {

	private int id;
	private String adresa;
	
	
	public CentruDonare(int id, String adresa) {
		super();
		this.id = id;
		this.adresa = adresa;
	}
	public int getId() {
		return id;
	}
	public String getAdresa() {
		return adresa;
	}
	
	
}
